public class Default_Value{

		//Global static variables
		static int integer;
		static byte var_byte;
		static short var_short_int;
		static long var_long_int;
		static double var_double;
		static char character;
		static boolean var_bool;
		public static void main(String[] args){
				System.out.println("int: "+integer);
				System.out.println("byte: "+var_byte);
				System.out.println("short: "+var_short_int);
				System.out.println("long: "+var_long_int);
				System.out.println("double: "+var_double);
				System.out.println("char: "+character);
				System.out.println("boolean: "+var_bool);
		}
}
